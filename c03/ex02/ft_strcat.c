char	*ft_strcat(char *dest, char *src)
{
	int	i;
	int	c;

	c = 0;
	i = 0;
	while (dest[i] != '\0')
	{
		i++;
	}
	while (src[c] != '\0')
	{
		dest[i] = src[c];
		i++;
		c++;
	}
	dest[i] = '\0';
	return (dest);
}
